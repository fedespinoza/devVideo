var video = document.querySelector('.mivideo');
var boton = document.querySelector('.reproducir');

function reproducir() {
    if (video.paused) {
        video.play()
        boton.innerHTML = '<i class="icon-pause"></i>'
    } else {
        video.pause()
        boton.innerHTML = '<i class="icon-play"></i>'
    }
}

function frenar() {
    video.currentTime = 0
    video.pause();
    boton.innerHTML = '<i class="icon-play"></i>'
}

function saltar(valor) {
    video.currentTime += valor

}

